#include <stdio.h>

int main() {
	//실수 표현
	//데이터 타입 --> float 형으로 선언된 변수
	printf("%f \n", 9.08); 
	printf("%f \n", 298.08467);
	printf("%5.1f \n", 298.08467);
	printf("%20.1f \n", 298.08467);


	//데이터 타입 ==> double 형으로 선언된 변수
	printf("%lf \n", 9.08); 
	printf("%lf \n", 298.08467);
	printf("%5.1lf \n", 298.08467);
	printf("%20.1lf \n", 298.08467);


}